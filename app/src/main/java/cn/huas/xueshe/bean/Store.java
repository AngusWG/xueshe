package cn.huas.xueshe.bean;


public class Store {

    private String storeName;
    private long storeScore;
    private long storeAve;
    private String storeLocation;
    private long storeConsume;
    private String storeRecommend;


    public String getStoreName() {
        return storeName;
    }

    public void setStoreName(String storeName) {
        this.storeName = storeName;
    }


    public long getStoreScore() {
        return storeScore;
    }

    public void setStoreScore(long storeScore) {
        this.storeScore = storeScore;
    }


    public long getStoreAve() {
        return storeAve;
    }

    public void setStoreAve(long storeAve) {
        this.storeAve = storeAve;
    }


    public String getStoreLocation() {
        return storeLocation;
    }

    public void setStoreLocation(String storeLocation) {
        this.storeLocation = storeLocation;
    }


    public long getStoreConsume() {
        return storeConsume;
    }

    public void setStoreConsume(long storeConsume) {
        this.storeConsume = storeConsume;
    }


    public String getStoreRecommend() {
        return storeRecommend;
    }

    public void setStoreRecommend(String storeRecommend) {
        this.storeRecommend = storeRecommend;
    }

}
